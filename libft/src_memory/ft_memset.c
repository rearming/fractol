#include <string.h>

void	*ft_memset(void *dest, int c, size_t n)
{
	char			*ptr;

	if (n == 0)
		return (dest);
	ptr = (char*)dest;
	while (n--)
	{
		*ptr = (char)c;
		ptr++;
	}
	return (dest);
}
